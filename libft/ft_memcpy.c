#include "libft.h"

void	*ft_memcpy(void *s1, const void *s2, size_t num)
{
	char	*a;

	a = (char*)s1;
	while (num > 0)
	{
		*a = *(char *)s2;
		a++;
		s2++;
		num--;
	}
	return (s1);
}
