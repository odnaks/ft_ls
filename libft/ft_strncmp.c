#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t num)
{
	while (*s1 == *s2 && *s1 && (num > 0))
	{
		s1++;
		s2++;
		num--;
	}
	if (num == 0)
		return (0);
	else
		return (*(unsigned char*)s1 - *(unsigned char*)s2);
	return (0);
}
